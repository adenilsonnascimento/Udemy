    <h1>Exemplos de Comentários em PHP</h1>
    <p>Este exemplo demonstra o uso de diferentes tipos de comentários no PHP, incluindo comentários de uma linha e de múltiplas linhas.</p>

    <!-- Comentário -->
    <?php
    // Comentário de uma linha
    echo 'Comando echo<br/>';

    # Comentário de uma linha
    echo print("Comando print<hr/>");

    /* 
    Comentário de múltiplas linhas.
    Este tipo de comentário permite que várias
    linhas sejam comentadas ao mesmo tempo.
    */
    ?>

    <h2>Informações Adicionais sobre Comentários em PHP</h2>
    <ul>
        <li><strong>Comentário de uma linha:</strong> Pode ser feito usando <code>//</code> ou <code>#</code>. É útil para adicionar notas curtas ou desativar uma linha de código.</li>
        <li><strong>Comentário de múltiplas linhas:</strong> Utiliza <code>/* ... */</code> e é ideal para comentários mais longos que abrangem várias linhas.</li>
    </ul>

    <p>Comentários são essenciais para tornar o código mais legível e compreensível, tanto para o desenvolvedor original quanto para outros que possam trabalhar no código no futuro.</p>
